// The primitives: string, number and boolean

// In TypeScript, there are three primary primitive types: string, number, and boolean. These types represent basic data types that are commonly used in programming. Let's explore each of these types in detail:

// - String
let message: string = "Hello, TypeScript!";

// - Number
let total: number = 100;

// - Boolean
let isLogged: boolean = true;

// Here we assigned a value to a variable using the colon (:) followed by the type. This is called a type annotation.
// The type annotation is used by the TypeScript compiler to analyze the code for type correctness.
